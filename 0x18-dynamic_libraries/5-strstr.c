/**
 *_strstr  - locate character in string
 *@haystack: pointer to main string
 *@needle: chars to find its substring
 *Return: Return substringas point of char
 */
char *_strstr(char *haystack, char *needle)
{
	if (haystack == 0)
		return (needle);
	else
		return (haystack);
}

